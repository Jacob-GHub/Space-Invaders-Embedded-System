void update_display(void)
{
    i2c_start();
    i2c_write(0x3C << 1);
    i2c_write(0x00);
    i2c_write(0x21);
    i2c_stop();
    i2c_start();
    i2c_write(0x3C << 1);
    i2c_write(0x00);
    i2c_write(0);
    i2c_stop();
    i2c_start();
    i2c_write(0x3C << 1);
    i2c_write(0x00);
    i2c_write(127);
    i2c_stop();

    i2c_start();
    i2c_write(0x3C << 1);
    i2c_write(0x00);
    i2c_write(0x22);
    i2c_stop();
    i2c_start();
    i2c_write(0x3C << 1);
    i2c_write(0x00);
    i2c_write(0);
    i2c_stop();
    i2c_start();
    i2c_write(0x3C << 1);
    i2c_write(0x00);
    i2c_write(7);
    i2c_stop();

    i2c_start();
    i2c_write(0x3C << 1);
    i2c_write(0x40);
    for (unsigned int i = 0; i < 1024; i++)
    {
        i2c_write(screen_buffer[i]);
    }
    i2c_stop();
}

void clear_display(void)
{
    for (unsigned int i = 0; i < 1024; i++)
    {
        screen_buffer[i] = 0;
    }
}

void draw_pixel(unsigned char x, unsigned char y, unsigned char color)
{
    if (color)
    {
        screen_buffer[x + (y / 8) * SCREEN_WIDTH] |= (1 << (y % 8));
    }
    else
    {
        screen_buffer[x + (y / 8) * SCREEN_WIDTH] &= ~(1 << (y % 8));
    }
}

void draw_rectangle(unsigned char x, unsigned char y, unsigned char w, unsigned char h, unsigned char color)
{
    for (unsigned char i = 0; i < w; i++)
    {
        for (unsigned char j = 0; j < h; j++)
        {
            draw_pixel(x + i, y + j, color);
        }
    }
}

