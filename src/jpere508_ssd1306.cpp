void draw_char(unsigned char x, unsigned char y, char c)
{
    for (unsigned char i = 0; i < 5; i++)
    {
        unsigned char line = pgm_read_byte(&font5x7[c - 32][i]);
        for (unsigned char j = 0; j < 7; j++)
        {
            if (line & (1 << j))
            {
                draw_pixel(x + i, y + j, 1);
            }
        }
    }
}

void draw_string(unsigned char x, unsigned char y, const char *str)
{
    while (*str)
    {
        draw_char(x, y, *str);
        str++;
        x += 6;
        if (x > 122)
            break;
    }
}

void draw_number(unsigned char x, unsigned char y, int num)
{
    char buffer[6];
    itoa(num, buffer, 10);
    draw_string(x, y, buffer);
}